#Normal Slicing
myString="Hello World"
mySlicedString=myString[3:7]
print(mySlicedString)
print(myString[3:7])

#Slicing from the begining to the given index.
print(myString[:9])

#Slicing till the end from the given index..
print(myString[5:])

#Negative indexing ...
a="Hello World."
print(a[-5:-4])


