#sorting alphanumerically
fruits=["apple","kiwi","mango","cherry"]
fruits.sort()
print(fruits)

#sorting the numbers
myNumbers=[500,400,300,200,100]
myNumbers.sort()
print(myNumbers)

#Mixed dataType not supported in sort
#mixedList=["mango",1,2,"cherry"]
#mixedList.sort()
#print(mixedList)
myNumbers.sort(reverse=True)
print(myNumbers)
fruits.sort(reverse=True)
print(fruits)

